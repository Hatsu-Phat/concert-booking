-- 31. Vé
CREATE TABLE VE
(
    MaVe            NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaDonMua        NUMBER REFERENCES DON_MUA (MaDonMua),
    MaHangVe        NUMBER REFERENCES HANG_VE (MaHangVe),
    MaGhe           NUMBER REFERENCES GHE_NGOI (MaGhe),
    MaLichDien      NUMBER REFERENCES LICH_DIEN (MaLichDien),
    DaBanLai        NUMBER(1, 0) DEFAULT 0 NOT NULL CHECK (DaBanLai IN (0, 1)),
    GiaBanLai       NUMBER(15, 2) CHECK (GiaBanLai >= 0),
    LinkQRCode      VARCHAR2(500),
    TrangThaiVe     VARCHAR2(50) DEFAULT N'Hiệu lực' NOT NULL CHECK (TrangThaiVe IN (N'Hiệu lực', N'Đã Check-in', N'Đã hủy', N'Chờ thanh toán')),
    ThoiGianDaBan   TIMESTAMP    DEFAULT CURRENT_TIMESTAMP,
    ThoiGianCheckIn TIMESTAMP,
    CONSTRAINT CK_VE CHECK ((DaBanLai = 0 AND TrangThaiVe IN (N'Đã Check-in', N'Đã hủy')) OR
                            (DaBanLai = 1 AND TrangThaiVe = N'Hiệu lực')),
    CONSTRAINT CK_TGCKIN CHECK (TrangThaiVe <> N'Đã Check-in' OR ThoiGianCheckIn IS NOT NULL),
    CONSTRAINT CK_BAN_LAI CHECK ((DaBanLai = 1 AND GiaBanLai > 0) OR (DaBanLai = 0)),
    CONSTRAINT CK_TGCKIN_TGDB CHECK (ThoiGianCheckIn >= ThoiGianDaBan)
);

