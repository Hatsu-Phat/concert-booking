
ALTER SESSION SET NLS_DATE_FORMAT = 'DD/MM/YYYY';
-- 1. Chức năng
CREATE TABLE CHUC_NANG
(
    MaChucNang  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenChucNang NVARCHAR2(255) NOT NULL
);

-- 2. Người dùng
CREATE TABLE NGUOI_DUNG
(
    MaNguoiDung   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    HoTen         NVARCHAR2(255)       NOT NULL,
    Email         VARCHAR2(100) UNIQUE NOT NULL,
    AnhDaiDienURL VARCHAR2(500),
    SoDienThoai   VARCHAR2(15) UNIQUE  NOT NULL,
    DaXacThuc     NUMBER(1) DEFAULT 0 NOT NULL CHECK (DaXacThuc IN (0, 1)),
    NgayTao       TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 3. Địa điểm
CREATE TABLE DIA_DIEM
(
    MaDiaDiem     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenDiaDiem    NVARCHAR2(255) NOT NULL,
    SucChua       NUMBER(6, 0)  DEFAULT 0 NOT NULL CHECK (SucChua >= 0),
    TinhThanh     NVARCHAR2(255) NOT NULL,
    PhuongXa      NVARCHAR2(255) NOT NULL,
    SoNhaTenDuong NVARCHAR2(255) NOT NULL
);

-- 4. Nhà tài trợ
CREATE TABLE NHA_TAI_TRO
(
    MaNhaTT  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenNhaTT NVARCHAR2(255) NOT NULL
);

-- 5. Nhóm quyền
CREATE TABLE NHOM_QUYEN
(
    MaNhomQuyen  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenNhomQuyen NVARCHAR2(100) NOT NULL,
    MoTa         CLOB
);

-- 6. Nghệ sĩ
CREATE TABLE NGHE_SI
(
    MaNgheSi      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenNgheSi     NVARCHAR2(255) NOT NULL,
    Email         VARCHAR2(100) UNIQUE,
    AnhDaiDienURL VARCHAR2(500),
    SoDienThoai   VARCHAR2(15) UNIQUE
);

-- 7. Chiến dịch khuyến mãi
CREATE TABLE CHIEN_DICH_KHUYEN_MAI
(
    MaChienDich  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TenChienDich NVARCHAR2(255) NOT NULL,
    ThoiDiemBD   TIMESTAMP      NOT NULL,
    ThoiDiemKT   TIMESTAMP      NOT NULL,
    TrangThai    NVARCHAR2(50) DEFAULT N'Chưa diễn ra' NOT NULL CHECK (TrangThai IN (N'Chưa diễn ra', N'Đang diễn ra', N'Tạm dừng', N'Đã kết thúc')),
    CONSTRAINT CK_CHIEN_DICH_TIME CHECK (ThoiDiemBD < ThoiDiemKT)
);

-- 8. Tài khoản
CREATE TABLE TAI_KHOAN
(
    MaTaiKhoan         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaNguoiDung        NUMBER REFERENCES NGUOI_DUNG (MaNguoiDung),
    TenDangNhap        VARCHAR2(100) UNIQUE NOT NULL,
    MatKhauMaHoa       VARCHAR2(255)        NOT NULL,
    TrangThai          NVARCHAR2(50) DEFAULT N'Hoạt động' NOT NULL CHECK (TrangThai IN (N'Hoạt động', N'Bị khóa', N'Bị cấm')),
    LanDangNhapCuoi    TIMESTAMP,
    DongYNhanMarketing NUMBER(1) DEFAULT 0 CHECK (DongYNhanMarketing IN (0, 1))
);

-- 9. Nhà tổ chức
CREATE TABLE NHA_TO_CHUC
(
    MaNhaToChuc      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaNguoiDaiDien   NUMBER REFERENCES NGUOI_DUNG (MaNguoiDung),
    MaTaiKhoan       NUMBER REFERENCES TAI_KHOAN (MaTaiKhoan),
    TenNhaToChuc     NVARCHAR2(255)      NOT NULL,
    MaSoThue         VARCHAR2(50) UNIQUE NOT NULL,
    ThongTinNganHang NVARCHAR2(255)      NOT NULL,
    EmailHoTro       VARCHAR2(100)
);

-- 10. Ví cá nhân
CREATE TABLE VI_CA_NHAN
(
    MaVi             NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaTaiKhoan       NUMBER REFERENCES TAI_KHOAN (MaTaiKhoan),
    SoDu             NUMBER(15, 2) DEFAULT 0 NOT NULL CHECK (SoDu >= 0),
    MatKhauThanhToan VARCHAR2(255)           NOT NULL,
    TrangThai        NVARCHAR2(50) DEFAULT N'Đang hoạt động' NOT NULL CHECK (TrangThai IN (N'Bị khóa', N'Đang hoạt động'))
);

-- 11. Mẫu chính sách hoàn tiền
CREATE TABLE MAU_CHINH_SACH_HOAN_TIEN
(
    MaChinhSachHT NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaTaiKhoan    NUMBER REFERENCES TAI_KHOAN (MaTaiKhoan),
    TenChinhSach  NVARCHAR2(255) NOT NULL,
    MoTa          CLOB
);

-- 12. Phạm vi quyền
CREATE TABLE PHAM_VI_QUYEN
(
    MaPhamVi   NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaChucNang NUMBER REFERENCES CHUC_NANG (MaChucNang),
    Them       NUMBER(1, 0) DEFAULT 0 NOT NULL CHECK (Them BETWEEN 0 AND 1),
    Sua        NUMBER(1, 0) DEFAULT 0 NOT NULL CHECK (Sua BETWEEN 0 AND 1),
    Xoa        NUMBER(1, 0) DEFAULT 0 NOT NULL CHECK (Xoa BETWEEN 0 AND 1),
    Xem        NUMBER(1, 0) DEFAULT 0 NOT NULL CHECK (Xem BETWEEN 0 AND 1),
    XuatFile   NUMBER(1, 0) DEFAULT 0 NOT NULL CHECK (XuatFile BETWEEN 0 AND 1)
);

-- 13. Mã giảm giá
CREATE TABLE MA_GIAM_GIA
(
    ID             NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaChienDich    NUMBER REFERENCES CHIEN_DICH_KHUYEN_MAI (MaChienDich),
    MaGiamGia      VARCHAR2(50) UNIQUE NOT NULL,
    LoaiGiam       NVARCHAR2(50) DEFAULT N'Theo phần trăm' NOT NULL CHECK (LoaiGiam IN (N'Theo phần trăm', N'Số tiền cố định')),
    SoLuongGiam    NUMBER(15, 2)       NOT NULL CHECK (SoLuongGiam >= 0),
    GiamToiDa      NUMBER(15, 2)       NOT NULL CHECK (GiamToiDa >= 0),
    LuotDungToiDa  NUMBER(10) NOT NULL CHECK (LuotDungToiDa >= 0),
    SoLuotDaSuDung NUMBER(10) DEFAULT 0 CHECK (SoLuotDaSuDung >= 0),
    CONSTRAINT CK_GIAM_GIA_LIMIT CHECK (SoLuotDaSuDung <= LuotDungToiDa),
    CONSTRAINT CK_LG_THEO_PHAN_TRAM CHECK ((LoaiGiam = N'Theo phần trăm' AND SoLuongGiam <= 100) OR
                                           LoaiGiam = N'Số tiền cố định' ),
    CONSTRAINT CK_GIAM_TOI_DA CHECK ((LoaiGiam = N'Số tiền cố định' AND SoLuongGiam <= GiamToiDa) OR LoaiGiam = N'Theo phần trăm')
);

-- 14. Quy tắc hoàn tiền
CREATE TABLE QUY_TAC_HOAN_TIEN
(
    MaQuyTac         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaChinhSachHT    NUMBER REFERENCES MAU_CHINH_SACH_HOAN_TIEN (MaChinhSachHT),
    SoGioTruocSuKien NUMBER(10) NOT NULL CHECK (SoGioTruocSuKien >= 0),
    TyLeHoanTien     NUMBER(5, 2) DEFAULT 0 NOT NULL CHECK (TyLeHoanTien BETWEEN 0 AND 100)
);

-- 15. Cấu hình nhóm quyền
CREATE TABLE CAU_HINH_NHOM_QUYEN
(
    MaNhomQuyen NUMBER REFERENCES NHOM_QUYEN (MaNhomQuyen),
    MaPhamVi    NUMBER REFERENCES PHAM_VI_QUYEN (MaPhamVi),
    PRIMARY KEY (MaNhomQuyen, MaPhamVi)
);

-- 16. Phân quyền nhóm
CREATE TABLE PHAN_QUYEN_NHOM
(
    MaTaiKhoan  NUMBER REFERENCES TAI_KHOAN (MaTaiKhoan),
    MaNhomQuyen NUMBER REFERENCES NHOM_QUYEN (MaNhomQuyen),
    PRIMARY KEY (MaTaiKhoan, MaNhomQuyen)
);

-- 17. Phân quyền tài khoản
CREATE TABLE PHAN_QUYEN_TAI_KHOAN
(
    MaTaiKhoan NUMBER REFERENCES TAI_KHOAN (MaTaiKhoan),
    MaPhamVi   NUMBER REFERENCES PHAM_VI_QUYEN (MaPhamVi),
    PRIMARY KEY (MaTaiKhoan, MaPhamVi)
);

-- 18. Sự kiện
CREATE TABLE SU_KIEN
(
    MaSuKien           NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MaDiaDiem          NUMBER REFERENCES DIA_DIEM (MaDiaDiem),
    MaChinhSachHT      NUMBER REFERENCES MAU_CHINH_SACH_HOAN_TIEN (MaChinhSachHT),
    MaNhaToChuc        NUMBER REFERENCES NHA_TO_CHUC (MaNhaToChuc),
    TenSuKien          NVARCHAR2(255) NOT NULL,
    ThoiGianBD         TIMESTAMP      NOT NULL,
    ThoiGianKT         TIMESTAMP      NOT NULL,
    ThoiGianMoBanVe    TIMESTAMP      NOT NULL,
    ThoiGianNgungBanVe TIMESTAMP      NOT NULL,
    MoTa               CLOB,
    TrangThai          NVARCHAR2(50) DEFAULT N'Chờ phê duyệt' NOT NULL CHECK (TrangThai IN
                                            (N'Chờ phê duyệt', N'Sắp diễn ra', N'Đang diễn ra', N'Đã kết thúc', N'Đã hủy')),
    TyLeHoaHong        NUMBER(5, 2) DEFAULT 0 NOT NULL CHECK (TyLeHoaHong BETWEEN 0 AND 100),
    QuyDinhSuKien      CLOB,
    CONSTRAINT CK_SK_TGSK CHECK (ThoiGianBD < ThoiGianKT),
    CONSTRAINT CK_SK_TGBV CHECK (ThoiGianMoBanVe < ThoiGianNgungBanVe),
    CONSTRAINT CK_SK_TGBV_TGBD CHECK (ThoiGianMoBanVe < ThoiGianBD),
    CONSTRAINT CK_SK_TGNBV_TGKT CHECK (ThoiGianNgungBanVe <= ThoiGianKT)
);

-- 19. Tài trợ
CREATE TABLE TAI_TRO
(
    MaSuKien   NUMBER REFERENCES SU_KIEN (MaSuKien),
    MaNhaTT    NUMBER REFERENCES NHA_TAI_TRO (MaNhaTT),
    HangTaiTro NVARCHAR2(50) DEFAULT N'Đồng' NOT NULL CHECK (HangTaiTro IN (N'Đồng', N'Bạc', N'Vàng', N'Kim Cương')),
    PRIMARY KEY (MaSuKien, MaNhaTT)
);

-- 20. Tham gia
CREATE TABLE THAM_GIA
(
    MaNgheSi    NUMBER REFERENCES NGHE_SI (MaNgheSi),
